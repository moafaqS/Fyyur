@app.route('/venues')
def venues():

  venues = Venue.query.all()

  # GET all unique location (state , city)
  location = set()
  for v in venues:
    location.add((v.state , v.city))

  data = []
  for l in location:
    venuesForlocation = Venue.query.filter_by(state=l[0]).filter_by(city=l[1]).all()
    venuesData = []
    for v in venuesForlocation:
      venuesData.append({
        "id": v.id,
        "name": v.name,
        "num_upcoming_shows": len(Show.query.filter(Show.venue_id==v.id).filter(Show.start_time>datetime.now()).all()),
      })
    data.append({
      "city": l[1],
      "state": l[0], 
      "venues": venuesData
    })
    
  return render_template('pages/venues.html', areas=data)

